/**
 * _isupper - Checks for uppercase characters.
 * @c: the int for the paramaters of my function.
 * Return: 1 if character is uppercase, 0 otherwise.
 */
int _isdigit(int c)
{
	if (c >= 48 && c <= 57)
	{
	return (1);
	}
	else
	return (0);
}
